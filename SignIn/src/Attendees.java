


public class Attendees {
	//Fields
	private String name;
	private String school;
	private String email;
	
	
	public Attendees(String n, String s, String e) {
		name = n;
		school = s;
		email = e;
	}
	
	public String getName(){
		return name;
	}
	
	public String getSchool(){
		return school;
	}
	
	public String getEmail(){
		return email;
	}
	

}
